/**
 * @brief ?????????????
 */
void run_by_button(void)
{
	// ?????????????????????1??2?????????1?????????????2???????
	BUTTON_e button = Button_waitPress(BUTTON_1 | BUTTON_2);
	SITE_e run_site = (button == BUTTON_1) ? SITE_RED : SITE_BLUE;
	// ??????????LED????
	if (run_site == SITE_RED)
		LED(1, 0, 0);
	else if (run_site == SITE_BLUE)
		LED(0, 0, 1);
	else
		LED(0, 1, 0);
	osDelay(300);
	// ???????3?????????????
	Button_waitPress(BUTTON_3);
	if (run_site == SITE_RED)
		run_red();
	else
		run_blue();
	while (1)
	{
		LED(1, 1, 1);
		osDelay(500);
		LED(0, 0, 0);
		osDelay(500);
	}
}
